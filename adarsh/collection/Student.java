
public class Student {
	int roolnumber,semister,marks,avaregemarks;
	String Name;
	public Student(int roolnumber,int semister, int marks, String Name) {	
		this.roolnumber = roolnumber;
		this.semister = semister;
		this.marks = marks;
		this.Name = Name;
	}

	public int getRoolnumber() {
		return roolnumber;
	}
	
	public int getSemister() {
		return semister;
	}
	
	
	public int getMarks() {
		return marks;
	}
	
	
	public String getName() {
		return Name;
	}

	
	public int getAvaregemarks() {
		return avaregemarks;
	}
	

	
	
	
}
